const apiURL = "https://overpass-api.de/api/interpreter?data=";

// send a GET request
const getData = (overpassQuery) => {
  axios({
    method: 'get',
    url: apiURL + encodeURIComponent(overpassQuery),
  }).then((response) => {
    const element = document.getElementById("data");
    element.textContent = (response.data);
    console.log(response.data)
  }).catch((error) => {
    console.error("Error while retrieving data from OSM: ", error)
  });
};

/*
The query below will return museums from London.
This has been generated by the overpass-turbo wizard.
*/
const overpassQuery = `
area(id:3600065606)->.searchArea;
nwr["tourism"="museum"](area.searchArea);
out geom;
`;

// returns only Sherlock Holmes Museum and shop
const sherlockHolmes = `
area(id:3600065606)->.searchArea;
node["tourism"="museum"]["name"="The Sherlock Holmes Museum and shop"](area.searchArea);
out geom;
`;

// returns only the Science Museum
const scienceMuseum = `
area(id:3600065606)->.searchArea;
way["tourism"="museum"]["name"="Science Museum"](area.searchArea);
out geom;
`;

// returns only the British Museum
const britishMuseum = `
area(id:3600065606)->.searchArea;
relation["tourism"="museum"]["name"="British Museum"](area.searchArea);
out geom;
`;
getData(overpassQuery);
